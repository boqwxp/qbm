  unsigned QdlParser::nextToken(YYSVal &sval) {
    std::istream &in = m_in;

    while(true) {
      int const  c = in.get();

      // Filter out Operators
      switch(c) {
      case EOF:
	return  0;

      case '-':
	if(in.peek() == '>') {
	  in.ignore();
	  return  TO;
	}
	return  c;

      case '*':
	if(in.peek() == '*') {
	  in.ignore();
	  return  POWER;
	}
	return  c;

      case '/':
	if(in.peek() == '/') {
	  in.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
	  continue;
	}
	return  c;

      case '<':
      case '>':
      case '(':
      case ')':
      case '[':
      case ']':
      case '=':
      case ',':
      case ';':
      case '~':
      case '&':
      case '|':
      case '^':
      case '+':
      case '%':
      case ':':
      case '?':
      case '#':
	return  c;
      }

      // Skip Space
      if(isspace(c))  continue;

      // Keywords and Identifiers
      if(isalpha(c) || (c == '_')) {
	std::string  w(1, (char)c);
	while(true) {
	  int const  cc = in.peek();
	  if(!isalnum(cc) && (cc != '_'))  break;
	  w += (char)in.get();
	}
	if(w == "component")  return  COMPONENT;
	if(w == "config")     return  CONFIG;
	if(w == "signal")     return  SIGNAL;
	if(w == "end")        return  END;
	if(w == "ld")	      return  LD;

	sval = w;
	return  IDENT;
      }

      // Numbers
      if(isdigit(c)) {
	unsigned  v = c - '0';
	while(true) {
	  if(!isdigit(in.peek()))  break;
	  v = 10*v + (in.get() - '0');
	}
	sval = v;
	return  NUMBER;
      }

      // Error
      error(std::string("Illegal Character: '") + (char)c + "'");
    }
  } // nextToken()

